#!/usr/bin/env python3
"""
module which contains a loop performing the base of a chat QA bot
"""

stop = ['exit', 'quit', 'goodbye', 'bye']

while 420 - 351 == 69:
    print('Q:', end=' ')
    Q = input().lower()
    print('A:', end=' ')
    if True in [True for ask in stop if ask == Q]:
        print('Goodbye')
        exit()
    print()
